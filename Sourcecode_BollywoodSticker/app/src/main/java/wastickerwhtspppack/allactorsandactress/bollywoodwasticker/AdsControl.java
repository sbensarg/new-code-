package wastickerwhtspppack.allactorsandactress.bollywoodwasticker;

public class AdsControl {
    public String FAN_BAnner_ADS;
    public String enable_banner;
    public String FAN_Intertitial_ADS;
    public String enable_inter;
    public String FAN_Native_Ads;
    public String enable_native;


    public AdsControl() {

    }

    public AdsControl(String FAN_BAnner_ADS, String enable_banner,
                      String FAN_Intertitial_ADS, String enable_inter,
                      String FAN_Native_Ads, String enable_native) {
        this.FAN_BAnner_ADS = FAN_BAnner_ADS;
        this.enable_banner = enable_banner;
        this.FAN_Intertitial_ADS = FAN_Intertitial_ADS;
        this.enable_inter = enable_inter;
        this.FAN_Native_Ads = FAN_Native_Ads;
        this.enable_native = enable_native;
    }
}
